//Function to find the data of nth node from the end of a linked list.
class Solution{
public:
    Node *reverse(Node *head)
    {
        Node *prev=NULL, *next=head, *curr=head;
        while(curr!=NULL)
        {
            next=curr->next;
            curr->next=prev;
            prev=curr;
            curr=next;
        }
        return prev;
    }
    int getNthFromLast(Node *head, int n)
    {
        Node *t=head;
        int size=0;
        while(t!=NULL)
        {
            t=t->next;
            size++;
        }
        if(size-n<0)    return -1;
        
        head=reverse(head);
        t=head;
        while(--n)
        {
            t=t->next;
        }
        return t->data;
    }
};



